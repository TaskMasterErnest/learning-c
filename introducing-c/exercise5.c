#include <stdio.h>

int main(void)
{
    int toes, toes_twice, toes_squared;
    toes = 10;

    toes_twice = toes * 2;
    toes_squared = toes * toes;

    printf("The value of toes is %d\n", toes);
    printf("the value of twice of toes is %d\n", toes_twice);
    printf("The value of toes squared is %d\n", toes_squared);

    return 0;
}
